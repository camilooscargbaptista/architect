function buildAgentCatalog(ctx: TemplateContext): string {
  const { stack } = ctx;
  const rows: string[] = [];

  rows.push('| Agente | Role | Capabilities |');
  rows.push('|--------|------|-------------|');
  rows.push('| [AGENT-ORCHESTRATOR](./agents/AGENT-ORCHESTRATOR.md) | coordination | DecomposiГ§ГЈo, dispatch, consolidaГ§ГЈo, quality gate |');

  if (stack.hasBackend) {
    rows.push(`| [${stack.primary.toUpperCase()}-BACKEND-DEVELOPER](./agents/${stack.primary.toUpperCase()}-BACKEND-DEVELOPER.md) | development | APIs, serviГ§os, lГіgica de negГіcio, migrations |`);
  }
  if (stack.hasFrontend) {
    const fw = stack.frameworks.find(f => ['Angular', 'Vue', 'Next.js', 'React'].includes(f)) || 'FRONTEND';
    rows.push(`| [${fw.toUpperCase().replace('.', '')}-FRONTEND-DEVELOPER](./agents/${fw.toUpperCase().replace('.', '')}-FRONTEND-DEVELOPER.md) | development | Componentes, state management, UX responsiva |`);
  }
  if (stack.hasMobile) {
    rows.push('| [FLUTTER-UI-DEVELOPER](./agents/FLUTTER-UI-DEVELOPER.md) | development | Screens, widgets, navegaГ§ГЈo, integraГ§ГЈo API |');
  }
  if (stack.hasDatabase) {
    rows.push('| [DATABASE-ENGINEER](./agents/DATABASE-ENGINEER.md) | development | Schema, migrations, indexaГ§ГЈo, performance |');
  }

  rows.push('| [SECURITY-AUDITOR](./agents/SECURITY-AUDITOR.md) | quality | AmeaГ§as STRIDE, OWASP, LGPD, vulnerabilidades |');
  rows.push('| [QA-TEST-ENGINEER](./agents/QA-TEST-ENGINEER.md) | quality | Planos de teste, BDD/TDD, cobertura, regressГЈo |');
  rows.push('| [TECH-DEBT-CONTROLLER](./agents/TECH-DEBT-CONTROLLER.md) | governance | DГ©bito tГ©cnico, refatoraГ§Гөes, metas de score |');

  return rows.join('\n');
}

function buildSituationalGuide(ctx: TemplateContext): string {
  const { stack } = ctx;
  const rows: string[] = [];

  rows.push('| AГ§ГЈo | Leia antes | Workflow | Agente principal |');
  rows.push('|------|-----------|---------|-----------------|');
  rows.push('| Nova feature | 00-general + PREFLIGHT | [new-feature](./workflows/new-feature.md) | ORCHESTRATOR |');
  rows.push('| Corrigir bug | 00-general + PREFLIGHT | [fix-bug](./workflows/fix-bug.md) | ORCHESTRATOR |');

  if (stack.hasBackend) {
    rows.push(`| Mexer em backend | 00-general + 01-architecture | new-feature ou fix-bug | ${stack.primary.toUpperCase()}-BACKEND |`);
  }
  if (stack.hasFrontend) {
    rows.push('| Mexer em frontend | 00-general + 01-architecture | new-feature ou fix-bug | FRONTEND |');
  }
  if (stack.hasMobile) {
    rows.push('| Mexer no app | 00-general | new-feature ou fix-bug | FLUTTER-UI |');
  }
  if (stack.hasDatabase) {
    rows.push('| Mexer em banco | 00-general + 01-architecture | new-feature | DATABASE-ENGINEER |');
  }

  rows.push('| SeguranГ§a | 02-security | вҖ” | SECURITY-AUDITOR |');
  rows.push('| Testes | 00-general | вҖ” | QA-TEST-ENGINEER |');
  rows.push('| RefatoraГ§ГЈo | QUALITY-GATES | вҖ” | TECH-DEBT-CONTROLLER |');
  rows.push('| Code review | CODE-REVIEW-CHECKLIST | [review](./workflows/review.md) | QA-TEST-ENGINEER |');

  return rows.join('\n');
}

